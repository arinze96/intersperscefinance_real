                                                    <p>Our values shape our vision, guide our daily decision making, and signal to the world
                                                        what to expect when you encounter Global Fund for Children. We hold ourselves to
                                                        these values to best serve our local partners and the children and youth they
                                                        support.
                                                    </p>
                                                    <h4 style="font-style:italics; color:#0f1341">Courage | Take risks for change</h4>
                                                    <p>When there’s an opportunity to spark big change, we’re willing to take smart risks
                                                        that others won’t. We recognize that social innovation requires thinking big and
                                                        breaking down barriers, so we strive to be courageous but not single-minded,
                                                        visionary but rooted in real progress.</p>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">EXCELLENCE | Take good to great</h4>
                                                    <p>In our interactions as a team and with partners, donors, and other collaborators, we
                                                        strive not just to meet commitments but to exceed expectations, making every GFC
                                                        experience better than anticipated.</p>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">LEARNING | Wonder, discover, reflect,
                                                        and adapt:</h4>
                                                    <p>We approach work with child like curiosity and enthusiasm for discovery, and we share
                                                        our experiences and resources generously. We innovate by learning from success and
                                                        failure, trying again, and encouraging others to do the same.</p>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">PARTNERSHIP | Serve partners first:
                                                    </h4>
                                                    <p>In our interactions as a team and with partners, donors, and other collaborators, we
                                                        strive not just to meet commitments but to exceed expectations, making every GFC
                                                        experience better than anticipated.</p>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">PASSION | Embrace passion and kindness:
                                                    </h4>
                                                    <p>We energize and inspire others by drawing on passion and kindness. In a world full of
                                                        stress and difficulty, a culture of enthusiasm, empathy, and collaboration helps us
                                                        thrive in our work.</p>
                    
                                                    <h1 style="font-style:italics; color:#0f1341">OUR MODEL</h1>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">We Find</h4>
                                                    <p>
                                                        We identify innovative organizations – typically in the early stages of their
                                                        development – that are run by local leaders working with children and youth around
                                                        the world.</p>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">We Fund</h4>
                                                    <p>We fund our partners’ life-changing programs for children and youth, as well as their
                                                        organizational development. We keep our grants flexible to maximize our impact and
                                                        to meet needs that other funders are not willing to support.</p>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">Together we strengthen.</h4>
                                                    <p>We advise, mentor, and guide our partners. We build mutual trust, accountability, and
                                                        enduring relationships. Our targeted capacity development helps our partners grow
                                                        stronger and more responsive to challenges on the ground.</p>
                    
                                                    <h4 style="font-style:italics; color:#0f1341">We build networks</h4>
                                                    <p>We connect our partners to each other and to national and regional networks. We bring
                                                        together brilliant minds to share knowledge, generate learning, fuel advocacy, and
                                                        build movements of social change.</p>
                    
                                                    <h1 style="font-style:italics; color:#0f1341">Safeguarding Policy</h1>
                                                    <p>MONETARY TRADES’s donation for Children partners with community-based organizations
                                                        around the world to help children and youth reach their full potential and advance
                                                        their rights. We are deeply committed to protecting those most at risk and to acting
                                                        as a role model for child- and youth-serving organizations.
                    
                                                        Although children and youth may be extremely resourceful and resilient, they are
                                                        also particularly vulnerable to abuse due to their lack of status and power, their
                                                        stage of development, and their reliance on adults.</p>
                    
                                                    <p>We recognize that: <br>
                    
                                                        Organizations and professionals working or in contact with children and youth have a
                                                        duty to ensure safety and wellbeing. <br>
                                                        The welfare of the child is a paramount consideration, as enshrined in the UN
                                                        Convention on the Rights of the Child. <br>
                                                        All children and youth have an equal right to protection, regardless of age,
                                                        disability, gender, racial heritage, religious belief, sexual orientation, or gender
                                                        identity. <br>
                                                        It is our responsibility to minimize the harm that may be done as a result of our
                                                        activities. <br>
                                                        Our Global Safeguarding Policy reflects our commitment to respect, promote, uphold,
                                                        and protect the rights of the child as set out in the UN Convention on the Rights of
                                                        the Child and in line with Monetary Trades values.</p>
                    
                                                    <h1 style="font-style:italics; color:#0f1341">OUR VISION</h1>
                                                    Our vision is that every child and young person has a safe and happy childhood, and the
                                                    foundations they need to thrive. We do this by working closely with children and their
                                                    families, from before they’re born until their twenties.
                                                    
                                                    Our work is split into three main areas: To make sure our work helps the most vulnerable
                                                    children and young people, we're pressing for change on three key themes:
                                                    
                                                    <h4 style="font-style:italics; color:#0f1341">Best start in life</h4>
                                                    <p>We know how important the early years are - and we’re here to help. From parenting advice to education and early intervention, we focus on improving children’s life chances.</p>
                                                    
                                                    <h4 style="font-style:italics; color:#0f1341">Good Mental Health;</h4>
                                                    <p>there's an increasing crisis in mental health among children and young people, with many are not getting the help they need.  Everyone has ups and downs in life. Our mental and emotional well-being experts work with families and schools to make sure children and parents feel able to cope with challenges when they arise.</p>
                                                    <h4 style="font-style:italics; color:#0f1341">A safe and loving home;</h4>
                                                    <p>We make sure more children feel part of a family that cares for them. We find homes for those without, protect children who have been abused or neglected, and help ensure young carers don’t miss out on childhood, and provide support to disabled children and their families, while children at risk often only get help when they've hit crisis point. Many in care, especially teenagers, face placement instability. 
                    
                                                        Many children and young people who have experienced domestic abuse do not get the support they need. How we help children and young people growing up is full of challenges but with a united effort we offer guidance, practical and emotional support. We do this in lots of different ways.</p>
                    
                                                        <p>Early years education that helps babies and toddlers learn and grow. <br> <br>
                                                            Protection from neglect and abuse, and help so that children can overcome past experiences. <br> <br>
                                                            Foster, adoption and residential services that provide safe, loving family homes. <br> <br>
                                                            Support for disabled young people, which builds confidence and independence. <br> <br>
                                                            Tackling mental health issues in children and young people. <br> <br>
                                                            Positive conversations about mental health, including The Blues Programme, our emotional wellbeing course for teenagers. <br> <br>
                                                            Giving young carers a break, so they can enjoy just being a child. <br> <br>
                                                            Employability and life skills programmes that prepare young people for their life as adults. <br> <br>
                                                            Campaigning so the voices of children and young people are heard. <br> <br>
                                                            Provide for critically ill children especially cancer affected children.</p> <br>
                                                            
                                                            <h1 style="font-style:italics; color:#0f1341">OUR IMPACT</h1>
                                                            <p>
                                                                Right now, Monetary Trades is helping the most vulnerable children across the Globe, We work to change the lives of vulnerable children starting at an early age. We’ve been doing this for about 3years. We’ve learnt a lot in this time - gathering experience and expertise that we use every day in more than 475 local services, in schools, and online. We make a real difference by doing what’s proven to work, and by coming up with new solutions. Last year, we reached 387,000 children, young people and families across the globe.
                                                                
                                                                We make sure children get the best start in life. Our foster, adoption and modern residential services find children a loving home. We protect those who have been abused or neglected, and help children who care for others or find themselves homeless. Projects like the Blues Programme give young people the tools to look after their mental and emotional wellbeing.
                                                                
                                                                We make an impact in so many ways. Here’s some of the ways we helped children, young people and families last year:
                                                            </p>
                                                            <h4 style="font-style:italics; color:#0f1341">13,500</h4>
                                                            <p>The number of children we supported through nurseries, out-of-school clubs, children’s centres, family centres and playgroups. That’s parenting programmes, ante-natal support and breastfeeding advice; drop-in groups. Plus specialised help to address or prevent problems that may arise in the first years of a child’s life.</p>
                                                            <h4 style="font-style:italics; color:#0f1341">8,000</h4>
                                                            <p>The number of children and young people we helped to find a stable and loving home. We’ve protected them from domestic violence, self-harm and suicide; built warm and supportive relationships with parents and carers; and helped them develop social skills and friendships. We’ve kept families together, or made sure children are cared for through fostering and adoption.</p>
                    
                                                            <h4 style="font-style:italics; color:#0f1341">11,000</h4>
                                                            <p>The number of children we’ve helped to improve their mental health in 2017/18. We’ve given support to young people who care for a family member, helped build practical life skills and friendships, turned around the lives of those who have committed a crime. Seventy-seven per cent of young people who took part in our enhanced Programme showed an improvement in their mental health and wellbeing. We’ve also provided medical support to critically ill children suffering from cancer,heart problems and other severe ailments,we provide these kids with the best medical attention ranging from medical surgeries,to drug procurement and provision of essential needs..</p>
                    
                                                            <h1 style="font-style:italics; color:#0f1341">Where your money goes</h1>
                                                            <p>In 2018/19, your donations helped us reach more than 387,000 children, young people and families across the globe
                    
                                                                Money raised by our incredible supporters – people like you – means we can protect and support more vulnerable and less privileged children
                                                                
                                                                For every $1 we spend, it goes directly towards helping children, young people and their families, and – with rising levels of poverty, abuse and neglect – this is vital.
                                                                
                                                                We step in early to stop abuse and neglect, find loving foster and adoptive homes, support young carers, and help young people out of trouble and into education and employment.</p>
